public class HelloWorld {
    // Method main adalah titik masuk utama program Java
    // Setiap program Java harus memiliki method main
    public static void main(String[] args) {
        // Mencetak teks ke layar menggunakan System.out.println
        System.out.println("Hello, World!");
        System.out.println("Selamat datang di pembelajaran Java!");
        
        // System.out.print tidak membuat baris baru
        System.out.print("Ini ");
        System.out.print("akan ");
        System.out.println("dalam satu baris");
    }
}


